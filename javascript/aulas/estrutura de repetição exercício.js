//A condição i <= 10 determina que o loop continuará até que o multiplicador i alcance 10.
//Após cada iteração do loop, o valor de i é incrementado em 1 (i++).
//Dentro do loop, é impresso o resultado da multiplicação entre i e numero, ou seja, é impressa a 
//tabuada de multiplicação para o número 7.
//Por exemplo, na primeira iteração (i = 1), será impresso 1 * 7, que é igual a 7. Na segunda iteração
